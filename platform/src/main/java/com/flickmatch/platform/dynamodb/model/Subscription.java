package com.flickmatch.platform.dynamodb.model;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.Data;
import lombok.ToString;

import java.time.LocalDate;

@DynamoDBTable(tableName = "Subscription")
@ToString
@Data
public class Subscription {

    private String subscriptionId;
    public static enum Status {
        valid,
        expired;
    }
    private Status status;
    private String subscriptionDetailsId;
    private LocalDate createdDate;
    private LocalDate lastUpdatedDate;

    @DynamoDBHashKey(attributeName="subscriptionId")
    @DynamoDBAutoGeneratedKey
    public String getSubscriptionId() {
        return subscriptionId;
    }
    @DynamoDBAttribute(attributeName = "subscriptionDetailsId")
    public String getSubscriptionDetailsId() {
        return subscriptionDetailsId;
    }

    public void setSubscriptionDetailsId(String subscriptionDetailsId) {
        this.subscriptionDetailsId = subscriptionDetailsId;
    }

    public void setSubscriptionId(String subscriptionId) {
        this.subscriptionId = subscriptionId;
    }

    @DynamoDBAttribute(attributeName = "createdDate")
    public LocalDate getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(LocalDate createdDate) {
        this.createdDate = createdDate;
    }

    @DynamoDBAttribute(attributeName = "lastUpdatedDate")
    public LocalDate getLastUpdatedDate() {
        return lastUpdatedDate;
    }

    public void setLastUpdatedDate(LocalDate lastUpdatedDate) {
        this.lastUpdatedDate = lastUpdatedDate;
    }


    @DynamoDBAttribute(attributeName = "status")
    @DynamoDBTypeConvertedEnum
    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

}
