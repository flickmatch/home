package com.flickmatch.platform.dynamodb.model;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import lombok.*;

@DynamoDBTable(tableName = "Passes")
@Builder
@AllArgsConstructor
@NoArgsConstructor
@Getter
@Setter
public class Pass {
    @DynamoDBHashKey(attributeName = "passId")
    @DynamoDBAutoGeneratedKey()
    private String passId;

    @DynamoDBAttribute(attributeName = "cityId")
    private String cityId;

    @DynamoDBAttribute(attributeName = "passType")
    private String passType;

    @DynamoDBAttribute(attributeName = "totalGames")
    private Integer totalGames;

    @DynamoDBAttribute(attributeName = "totalDays")
    private Integer totalDays;

    @DynamoDBAttribute(attributeName = "title")
    private String title;

    @DynamoDBAttribute(attributeName = "price")
    private Integer price;

    @DynamoDBAttribute(attributeName = "status")
    private String status;
}
